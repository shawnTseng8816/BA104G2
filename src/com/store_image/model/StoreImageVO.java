package com.store_image.model;

public class StoreImageVO implements java.io.Serializable {

	private String img_num;
	private String sto_num;
	private byte[] img;
	
	
	public String getImg_num() {
		return img_num;
	}
	public void setImg_num(String img_num) {
		this.img_num = img_num;
	}
	public String getSto_num() {
		return sto_num;
	}
	public void setSto_num(String sto_num) {
		this.sto_num = sto_num;
	}
	public byte[] getImg() {
		return img;
	}
	public void setImg(byte[] img) {
		this.img = img;
	}
	
	

	
	
	
	
	
}
